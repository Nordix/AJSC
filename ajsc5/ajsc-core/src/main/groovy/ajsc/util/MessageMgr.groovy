					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.trace("${msgCode}: ${getMessageText(msgCode)} ${formatExceptionDetails(exception)}", msgArgs)
					else
						logger?.trace("${msgCode}: ${getMessageText(msgCode)} ${formatExceptionDetails(exception)}")
				}
				break;
			default:
				if(logger?.isInfoEnabled()) {
					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.info("${msgCode}: ${getMessageText(msgCode)} ${formatExceptionDetails(exception)}", msgArgs)
					else
						logger?.info("${msgCode}: ${getMessageText(msgCode)} ${formatExceptionDetails(exception)}")
				}
				break;
			
			
			}
		}
	}
	
	static String formatExceptionDetails(Exception exception){
		def msg = ""

		if (exception != null){
			StringWriter sw = new StringWriter();
			PrintWriter pw = new PrintWriter(sw);
			StackTraceUtils.printSanitizedStackTrace(exception, pw)
			String stackTrace = sw.toString()
			String causedBy = null

			Throwable t = StackTraceUtils.extractRootCause(exception)
			if (t) {
				String rootMessage = t.toString()
				if (rootMessage)
					causedBy = rootMessage


			}
			msg =	" - EXCEPTION: ${exception.getMessage()}\nCAUSED BY: ${causedBy}  \nSTACKTRACE: ${stackTrace}"
		}
		return msg

	}
	
