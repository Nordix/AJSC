	static void logMessage(logger, level, messageMap, Object[] msgArgs=null) {
		if(logger instanceof org.slf4j.Logger) {
		switch(level) {
			case 'error':
			def msgCode = getMessageCode(messageMap)
			if(msgArgs!=null)
				logger?.error("${msgCode}: ${getMessageText(msgCode)}", msgArgs)
			else
				logger?.error("${msgCode}: ${getMessageText(msgCode)}")
				break;
			case 'warn':
				if(logger?.isWarnEnabled()) {
					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.warn("${msgCode}: ${getMessageText(msgCode)}", msgArgs)
					else
						logger?.warn("${msgCode}: ${getMessageText(msgCode)}")
				}
				break;
			case 'info':
				if(logger?.isInfoEnabled()) {
					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.info("${msgCode}: ${getMessageText(msgCode)}", msgArgs)
					else
						logger?.info("${msgCode}: ${getMessageText(msgCode)}")
				}
				break;
			case 'debug':
				if(logger?.isDebugEnabled()) {
					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.debug("${msgCode}: ${getMessageText(msgCode)}", msgArgs)
					else
						logger?.debug("${msgCode}: ${getMessageText(msgCode)}")
				}
				break;
			case 'trace':
				if(logger?.isTraceEnabled()) {
					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.trace("${msgCode}: ${getMessageText(msgCode)}", msgArgs)
					else
						logger?.trace("${msgCode}: ${getMessageText(msgCode)}")
				}
				break;
			default:
				if(logger?.isInfoEnabled()) {
					def msgCode = getMessageCode(messageMap)
					if(msgArgs!=null)
						logger?.info("${msgCode}: ${getMessageText(msgCode)}", msgArgs)
					else
						logger?.info("${msgCode}: ${getMessageText(msgCode)}")
				}
				break;
			
			
			}
		}
